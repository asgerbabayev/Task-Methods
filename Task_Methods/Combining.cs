using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Task_Methods
{
    class Combining
    {
        public string StringMethod(string a, string b)
        {
            string result = a + b;
            return result;
        }
        public string StringMethod(string a, string b, string c)
        {
            string result = a + b + c;
            return result;
        }
    }
}
